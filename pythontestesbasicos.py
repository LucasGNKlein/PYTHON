# Função que ordena utilizando o método insertion sort
def insertion_sort(arr):
    n = len(arr)

    # Percorre todos os elementos da lista, exceto o primeiro
    for i in range(1, n):

        # Assume que o elemento atual pertence à sublista ordenada
        key = arr[i]
        j = i-1

        # Move elementos da sublista ordenada à direita do elemento atual, se necessário
        while j >= 0 and key < arr[j]:
            arr[j + 1] = arr[j]
            j -= 1

        # Insere o elemento atual na posição correta na sublista ordenada
        arr[j + 1] = key

# Exemplo de uso
lista = [64, 34, 25, 12, 22, 11, 90]
