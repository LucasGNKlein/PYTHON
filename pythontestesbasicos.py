def selection_sort(arr):
    n = len(arr)
    # Percorre todos os elementos da lista
    for i in range(n):
        # Assume que o menor elemento é o primeiro elemento da sublista ainda não ordenada
        min_index = i
        # Percorre a sublista ainda não ordenada para encontrar o menor elemento
        for j in range(i+1, n):
            # Se o elemento atual for menor que o menor elemento encontrado até agora
            if arr[min_index] > arr[j]:
                # Atualiza o índice do menor elemento
                min_index = j
        # Troca o menor elemento encontrado com o primeiro elemento da sublista ainda não ordenada
        arr[i], arr[min_index] = arr[min_index], arr[i]

# Exemplo de uso
